package PraktikumPertemuan2;
public class Struktural {
    public static void main(String[] args) {
        
    //Variabel Luas Lingkaran
    double phi = 3.14;
    double jari2 = 10;
    
    //Variabel Luas Persegi Panjang
    double lebar = 10;
    double panjang = 20;
    
    //Hitunglah Luas Lingkaran
    double luasL = phi * (jari2 * jari2);
    
    //Hitunglah Luas Persegi Panjang
    double luasP = lebar * panjang;
    
        System.out.println("Luas Lingkaran = " + luasL);
        System.out.println("Luas Lingkaran = " + luasP);
    
}
    
}
